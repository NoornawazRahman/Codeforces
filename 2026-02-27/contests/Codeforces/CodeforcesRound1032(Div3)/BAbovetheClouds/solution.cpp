void solve() {
    int n;
    string s;
    cin >> n >> s;
    umap<char, int> freq;
    for (int i = 0; i < n; i++) {
        freq[s[i]]++;
    }
    for (int i = 1; i < n - 1; i++) {
        if (freq[s[i]] > 1) {
            cout << "Yes" << nl;
            return;
        }
    }
    cout << "No" << nl;
}
