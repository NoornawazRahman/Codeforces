void solve() {
    int a, b, c;
    cin >> a >> b >> c;
    int mini = min({a, b, c});
    debug(mini);
    int ops = 0;
    if (a % mini == 0 && b % mini == 0 && c % mini == 0) {
        ops += a / mini - 1;
        ops += b / mini - 1;
        ops += c / mini - 1;
    } else {
        no();
        return;
    }
    if (ops <= 3) {
        yes();
    } else {
        no();
    }
}
