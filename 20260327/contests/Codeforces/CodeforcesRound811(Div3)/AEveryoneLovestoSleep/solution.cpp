void solve() {
    int n;
    pair<int, int> p;
    cin >> n >> p.first >> p.second;
    vector<pair<int, int>> v(n);
    for (int i = 0; i < n; i++) {
        cin >> v[i].first >> v[i].second;
    }
    pair<int, int> ans = {60, 60};
    for (int i = 0; i < n; i++) {
        pair<int, int> diff;
        diff.first = v[i].first - p.first;
        diff.second = v[i].second - p.second;

        if (diff.second < 0) {
            diff.second += 60;
            diff.first--;
        }
        if (diff.first < 0) {
            diff.first += 24;
        }

        if (ans > diff) {
            ans = diff;
        }
    }
    cout << ans.first << sp << ans.second << nl;
}
